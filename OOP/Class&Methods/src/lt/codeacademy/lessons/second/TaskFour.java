package lt.codeacademy.lessons.second;

public class TaskFour {



    public  int triangleArea (int a, int b){
        return (a*b)/2;
    }

    public int rectangleArea (int a, int b) {
        return a * b;
    }

    public int squareArea (int a) {
        return a^2;
    }

    public float circleArea(int a) {

        float pi = 3.1415f;
        return pi*(a^2);

    }
}
